type TestCaseAppsec struct {
	*echo.Echo
	addr string
}

func (tc *TestCaseAppsec) Setup(_ context.Context, t *testing.T) {
	if runtime.GOOS == "windows" {
		t.Skip("appsec does not support Windows")
	}
	if ok, err := libddwaf.Usable(); !ok {
		t.Skip("WAF is not available:", err)
	}

	t.Setenv("DD_APPSEC_RULES", "../testdata/request-body.json")
	t.Setenv("DD_APPSEC_ENABLED", "true")
	t.Setenv("DD_APPSEC_WAF_TIMEOUT", "1h")

	tc.Echo = echo.New()
	tc.Echo.Logger.SetOutput(io.Discard)

	tc.Echo.GET("/ping", func(c echo.Context) error {
		type body struct {
			Message string `json:"message"`
		}

		var reqBody body
		if err := c.Bind(&reqBody); err != nil {
			return err
		}

		return c.JSON(http.StatusOK, map[string]any{"message": "pong"})
	})
	tc.addr = fmt.Sprintf("127.0.0.1:%d", net.FreePort(t))

	go func() { assert.ErrorIs(t, tc.Echo.Start(tc.addr), http.ErrServerClosed) }()
	t.Cleanup(func() {
		// Using a new 10s-timeout context, as we may be running cleanup after the original context expired.
		ctx, cancel := context.WithTimeout(context.Background(), 10*time.Second)
		defer cancel()
		require.NoError(t, tc.Echo.Shutdown(ctx))
	})
}

func (tc *TestCaseAppsec) Run(_ context.Context, t *testing.T) {
	resp, err := http.Post("http://"+tc.addr+"/ping", "application/json", strings.NewReader(`{"message":"$globals"}`))
	require.NoError(t, err)
	require.Equal(t, http.StatusForbidden, resp.StatusCode)
}

